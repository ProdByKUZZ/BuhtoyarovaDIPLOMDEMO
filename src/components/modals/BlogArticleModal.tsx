export function BlogArticleModal({ isOpen, onClose, postId }: BlogArticleModalProps) {
  const post = blogPosts.find((p) => p.id === postId);

  if (!post) return null;

  const content = articleContent[post.id] || "Полный текст статьи скоро будет опубликован.";

  const renderMarkdown = (text: string) => {
    const lines = text.split('\n');
    return lines.map((line, index) => {
      if (line.startsWith('## ')) {
        return <h2 key={index} className="text-2xl font-heading font-bold text-primary mb-4 mt-6">{line.slice(3)}</h2>;
      }
      if (line.startsWith('### ')) {
        return <h3 key={index} className="text-xl font-heading font-bold text-foreground mb-3 mt-4">{line.slice(4)}</h3>;
      }
      if (line.startsWith('**') && line.endsWith('**')) {
        return <p key={index} className="font-semibold text-foreground mb-2">{line.slice(2, -2)}</p>;
      }
      if (line.startsWith('- ')) {
        return <li key={index} className="ml-4 text-muted-foreground mb-1">{line.slice(2)}</li>;
      }
      if (line.match(/^\d+\./)) {
        return <li key={index} className="ml-4 text-muted-foreground mb-1">{line.slice(line.indexOf('.') + 2)}</li>;
      }
      if (line.startsWith('🚨') || line.startsWith('⚠️')) {
        return <p key={index} className="text-foreground mb-2">{line}</p>;
      }
      if (line.trim() === '') {
        return <br key={index} />;
      }
      // Handle inline bold
      const boldRegex = /\*\*(.*?)\*\*/g;
      const parts = line.split(boldRegex);
      if (parts.length > 1) {
        return (
          <p key={index} className="text-muted-foreground mb-2">
            {parts.map((part, i) => 
              i % 2 === 1 ? <strong key={i} className="text-foreground">{part}</strong> : part
            )}
          </p>
        );
      }
      return <p key={index} className="text-muted-foreground mb-2">{line}</p>;
    });
  };

  return (
    <Dialog open={isOpen} onOpenChange={onClose}>
      <DialogContent className="sm:max-w-4xl max-h-[90vh] overflow-y-auto">
        <DialogHeader>
          <DialogTitle className="sr-only">{post.title}</DialogTitle>
        </DialogHeader>

        <article className="space-y-6">
          {/* Header Image */}
          <div className="relative rounded-lg overflow-hidden h-64">
            <img
              src={post.image}
              alt={post.title}
              className="w-full h-full object-cover"
            />
            <div className="absolute inset-0 bg-gradient-to-t from-foreground/80 to-transparent" />
            <div className="absolute bottom-4 left-4 right-4">
              <span className="medical-badge mb-2 inline-block">{post.category}</span>
              <h1 className="text-2xl font-heading font-bold text-primary-foreground">
                {post.title}
              </h1>
            </div>
          </div>

          {/* Meta */}
          <div className="flex flex-wrap items-center gap-4 text-sm text-muted-foreground border-b border-border pb-4">
            <div className="flex items-center gap-2">
              <User className="w-4 h-4" />
              <span>{post.author}</span>
              <span className="text-accent">({post.authorRole})</span>
            </div>
            <div className="flex items-center gap-2">
              <Calendar className="w-4 h-4" />
              <span>{post.date}</span>
            </div>
            <div className="flex items-center gap-2">
              <Clock className="w-4 h-4" />
              <span>{post.readTime}</span>
            </div>
          </div>

          {/* Content */}
          <div className="prose max-w-none">
            {renderMarkdown(content)}
          </div>

          {/* Footer */}
          <div className="flex justify-center pt-6 border-t border-border">
            <Button variant="outline" onClick={onClose} className="gap-2">
              <ArrowLeft className="w-4 h-4" />
              Вернуться к статьям
            </Button>
          </div>
        </article>
      </DialogContent>
    </Dialog>
  );
}
